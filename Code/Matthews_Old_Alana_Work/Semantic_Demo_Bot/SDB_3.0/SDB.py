    def evaluate_similarity(self,Expected,Actual):

        #first, transform sentences into vectors (my thanks to https://masongallo.github.io/machine/learning,/python/2016/07/29/cosine-similarity.html)

        #now simplyify them

        Expected = self.simplify_sentence(Expected)

        Actual = self.simplify_sentence(Actual)

        #split the sentences into space-separated arrays of word strings

        print("now comparing: " + Expected + " to: " + Actual)

        Expected_Sentence = Expected.split(' ')

        if '' in Expected_Sentence:
            Expected_Sentence.remove('')#get rid of empties

        Actual_Sentence = Actual.split(' ')

        if '' in Actual_Sentence:
            Actual_Sentence.remove('')#get rid of empties

        #now get the set (actually an array, it will need to be ordered) of all unique words

        Unique_Words = []

        for temp in Expected_Sentence:
            if temp in Unique_Words:
                pass
            else:
                Unique_Words.append(temp)#adds words not yet added
                print("added the word: " + temp + " to the Unique wordlist")

        for temp in Actual_Sentence:
            if temp in Unique_Words:
                pass
            else:
                Unique_Words.append(temp)#adds words not yet added
                print("added the word: " + temp + " to the Unique wordlist")

        #now get two vectors, representing the count of how many times each word appears n each sentence (this may be quite slow, later versions will try to increase performance

        Expected_Vector = [0] * len(Unique_Words)#thanks to https://stackoverflow.com/questions/983699/initialise-a-list-to-a-specific-length-in-python

        Actual_Vector = [0] * len(Unique_Words)#thanks to https://stackoverflow.com/questions/983699/initialise-a-list-to-a-specific-length-in-python

        for temp in Expected_Sentence:
            Expected_Vector[Unique_Words.index(temp)] += 1#increases the value of that index by one, representing a shift along that word-dimension in vector-space

        for temp in Actual_Sentence:
            Actual_Vector[Unique_Words.index(temp)] += 1#increases the value of that index by one, representing a shift along that word-dimension in vector-space

        #The equation is as follows:  cos(θ) = (A·B)/(|A|*|B|)

        dot_product = numpy.dot(Expected_Vector,Actual_Vector)

        len_product = numpy.linalg.norm(Expected_Vector)*numpy.linalg.norm(Actual_Vector)

        cos_val = dot_product/len_product     

        print("cosine value for this iteration was: " + str(cos_val))

        #cosine value is useful, but doesn't give us everything. It would also be prudent to compare length

        len_ratio = min([len(Expected_Sentence),len(Actual_Sentence)])/max([len(Expected_Sentence),len(Actual_Sentence),1])#gets a value for size ratio that is, AT MOST, 1.0

        print("length ratio for this iteration was: " + str(len_ratio))

        #NOW GET SEMANTIC ANALYSIS!!

        sem_val = self.evaluate_semantics(Expected_Sentence, Actual_Sentence)#this returns a value from 0.0 to infinity.

        #sometimes, the semantic value returns "inf" if there is an unknown word. This should (for now) be turned into zero

        if str(sem_val) == "inf":
            sem_val = 0.0

        #now the semantic value needs to be turned into a number between zero and one

        #sigmoid of (0) is 0.5, sigmoid of five is 0.99

        #I need 0 to translate into perfection, so...

        sem_val = (self.sigmoid(sem_val) - 0.5)*2.0#this ensures that zero is perfect, and higher numbers still trend towards 1

        sem_val = 1.0 - sem_val

        print("semantic value for this iteration was: " + str(sem_val))

        #SEMANTIC ANALYSIS COMPLETE!

        return len_ratio*cos_val*sem_val#this will be no higher than 1.0, and no lower than -1.0

